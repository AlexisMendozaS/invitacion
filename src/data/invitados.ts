// Función para buscar invitado por código
export interface Invitado {
  id: number;
  nombre: string;
  mesa: number;
  adultos: number;
  niños: number;
  codigo: string;
}

export const buscarInvitadoPorCodigo = (codigo: string): Invitado | undefined => {
  return invitados.find((invitado: Invitado) => invitado.codigo === codigo);
};

// Función para obtener todos los invitados de una mesa
export interface InvitadoPorMesa {
  id: number;
  nombre: string;
  mesa: number;
  adultos: number;
  niños: number;
  codigo: string;
}

export const obtenerInvitadosPorMesa = (numeroMesa: number): InvitadoPorMesa[] => {
  return invitados.filter((invitado: InvitadoPorMesa) => invitado.mesa === numeroMesa);
};

// Función para obtener estadísticas generales
export const obtenerEstadisticas = () => {
    const totalInvitados = invitados.length;
    const totalAdultos = invitados.reduce((sum, inv) => sum + inv.adultos, 0);
    const totalNiños = invitados.reduce((sum, inv) => sum + inv.niños, 0);
    const totalPersonas = totalAdultos + totalNiños;
    const mesas = [...new Set(invitados.map(inv => inv.mesa))].sort((a, b) => a - b);
    
    return {
        totalInvitados,
        totalAdultos,
        totalNiños,
        totalPersonas,
        mesas: mesas.length,
        listaMesas: mesas
    };
};

